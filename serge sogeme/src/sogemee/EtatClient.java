    public void remplirClient(){
        DefaultComboBoxModel mod=new DefaultComboBoxModel();
        try {
            Statement s=sogemee.SOGEMEE.c.conn.createStatement();
            ResultSet r=s.executeQuery("select idClient,nomClient,prenomClient from client where suppr=0");
            while(r.next()){
                Client c=new Client(r.getInt(1),r.getString(2),r.getString(3));
                clients.add(c);
                mod.addElement(c.getNomClient()+" "+c.getPrenomClient());
            }
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex);
        }
        client.setModel(mod);
    }

