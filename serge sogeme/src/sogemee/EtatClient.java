    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int ind=client.getSelectedIndex();
        if(ind==-1){
            JOptionPane.showMessageDialog(rootPane,"Veuillez choisir le client svp");
            return;
        }
        Client c=(Client) clients.get(ind);
        
        try {
            JasperDesign jasperdesign = JRXmlLoader.load("src/sogemee/achatClient.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(jasperdesign);
            Map param = new HashMap();
           param.put("id",(short)c.getIdClient());
            JasperPrint jp = JasperFillManager.fillReport(jr, param, sogemee.SOGEMEE.c.getConn());
            JasperViewer.viewReport(jp,false);
           }
            catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
            }
        this.dispose();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

