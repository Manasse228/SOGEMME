    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        SimpleDateFormat format=new SimpleDateFormat("yyyy-MM-dd");
        
        int ind=fournisseur.getSelectedIndex();
        if(ind==-1){
            JOptionPane.showMessageDialog(rootPane,"Veuillez choisir le client svp");
            return;
        }
        try{
            deb.commitEdit();
            fin.commitEdit();
        
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        Date debut=(Date) deb.getValue();
        Date fini=(Date) fin.getValue();
        if(debut.after(fini)){
            JOptionPane.showMessageDialog(rootPane,"La date de debut est apr√®s la date de fin ");
            return;
        }
        Fournisseur f= (Fournisseur) fournisseurs.get(ind);
        
        try {
            JasperDesign jasperdesign = JRXmlLoader.load("src/sogemee/achatFournisseur.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(jasperdesign);
            Map param = new HashMap();
           param.put("id",(short)f.getIdFournisseur());
           param.put("deb",format.format(debut));
           param.put("fin",format.format(fini));
           
            JasperPrint jp = JasperFillManager.fillReport(jr, param, sogemee.SOGEMEE.c.getConn());
            JasperViewer.viewReport(jp,false);
           }
            catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
            }
        this.dispose();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

