     public void remplirFournisseur(){
        DefaultComboBoxModel mod=new DefaultComboBoxModel();
        try {
            Statement s=sogemee.SOGEMEE.c.conn.createStatement();
            ResultSet r=s.executeQuery("select idFournisseur,nomFournisseur from fournisseur where suppr=0");
            while(r.next()){
                Fournisseur f=new Fournisseur(r.getInt(1),r.getString(2));
                fournisseurs.add(f);
                mod.addElement(f.getNomFournisseur());
            }
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex);
        }
        fournisseur.setModel(mod);
    }

